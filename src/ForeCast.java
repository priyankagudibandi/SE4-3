

public class ForeCast {

	private String degreeCode;
	private String expectedNumOfStudents;
	
	
	
	public ForeCast() {
	}

	public ForeCast(String degreeCode, String expectedNumOfStudents) {
		super();
		this.degreeCode = degreeCode;
		this.expectedNumOfStudents = expectedNumOfStudents;
	}

	public String getDegreeCode() {
		return degreeCode;
	}


	public void setDegreeCode(String degreeCode) {
		this.degreeCode = degreeCode;
	}


	public String getExpectedNumOfStudents() {
		return expectedNumOfStudents;
	}


	public void setExpectedNumOfStudents(String expectedNumOfStudents) {
		this.expectedNumOfStudents = expectedNumOfStudents;
	}




	
	
	
}
